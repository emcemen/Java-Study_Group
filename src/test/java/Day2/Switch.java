package Day2;

public class Switch {
    public static void main(String[] args) {

        String day = "monday";


        switch(day){

            case "Monday":
                System.out.println("Today is Monday");
                break;
            case "Tuesday":
                System.out.println("Today is Monday");
                break;
            case"Wednesday":
                System.out.println("Today is Monday");
                break;
            case"Thursday":
                System.out.println("Today is Monday");
                break;
            case"Friday":
                System.out.println("Today is Friday");
                break;
            case"Saturday":
                System.out.println("Today is Monday");
                break;
            case"Sunday":
                System.out.println("Today is Monday");
                break;
            default:
                System.out.println("invalid day");


        }
    }
}
